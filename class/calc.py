a=int(input("Enter the first number:"))
b=int(input("Enter the second number:"))
c=input("choose option(+,-,*,/)")
if(c=="+"):
   print("Result:",a+b)
elif(c=="-"):
   print("Result:",a-b)
elif(c=="*"):
   print("Result:",a*b)
elif(c=="/"):
   print("Result:",a/b)
else:
    print("invalid syntax")   

